def aplicar_estilos(root):
    # Estilo del fondo
    root.configure(bg='#f0f0f0')

    # Aquí puedes agregar más personalización para los elementos
    style = {
        "bg_color": "#f0f0f0",
        "text_color": "#333",
        "button_color": "#4CAF50",
        "button_text_color": "white",
    }

    # Retorna el diccionario con los estilos
    return style
